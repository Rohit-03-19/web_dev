/** Switch statemnet */

// Stimulate a traffic light using switch

let color = "red";

switch (color) {
  case "red":
    console.log("STOP! the light is red.");
    break;

  case "yellow":
    console.log("Caution! the light is yellow");
    break;

  case "green":
    console.log("GO AHEAD!");
    break;

  default:
    console.log("Invalid Color");
    break;
}
